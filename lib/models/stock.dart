class Stock {
  final String uid;
  final List<String> url;
  final String flowerType;
  final int stemLength;
  final int quantity;
  final int flowerColour;
  final String dateAdded;
  final String companyName;

  Stock({
    this.uid,
    this.url,
    this.flowerType,
    this.stemLength,
    this.quantity,
    this.flowerColour,
    this.dateAdded,
    this.companyName,
  });
}
